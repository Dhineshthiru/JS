//ARITHMETIC-OPERATORS

let x = 5;
let y = 3;

// Addition
console.log('x + y = ', x + y);

// Subtraction
console.log('x - y = ', x - y);

// Multiplication
console.log('x * y = ', x * y);

// Division
console.log('x / y = ', x / y);

// Remainder
console.log('x % y = ', x % y);

// Increment
console.log('++x = ', ++x);
console.log('x++ = ', x++);
console.log('x = ', x);

// Decrement
console.log('--x = ', --x); 
console.log('x-- = ', x--); 
console.log('x = ', x);

//Exponentiation
console.log('x ** y =', x ** y);


//COMPARISION-OPERATOR

// equal operator
console.log(2 == 2); 
console.log(2 == '2');

// not equal operator
console.log(3 != 2); 
console.log('hello' != 'Hello');

// strict equal operator
console.log(2 === 2); 
console.log(2 === '2');

// strict not equal operator
console.log(2 !== '2'); 
console.log(2 !== 2);

//LOGICAL-OPERATOR

// Logical AND
console.log(true && true); 
console.log(true && false);

// Logical OR
console.log(true || false); 

// Logical NOT
console.log(!true);

//STRING-OPERATOR

// Concatenation operator
console.log('Hello' + 'World');
let a = 'JavaScript';
a += 'programs'; 
console.log(a);